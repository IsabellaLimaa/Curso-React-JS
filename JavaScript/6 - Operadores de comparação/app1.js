//7 - Operadores de comparação

//IGUAL ==
console.log("10 == 10:", 10 == 10);
console.log("11 == 10:", 11 == 10);
console.log("a == a:", "a" == "a");
console.log("b == a:", "b" == "a");
console.log("10 == 10:", "10" == 10); //analisa só o conteúdo

console.log("=================================");

//DIFERENTE
console.log("DIFERENTE");

console.log("10 != 10:", 10 != 10);
console.log("11 != 10:", 11 != 10);
console.log("a != a:", "a" != "a");
console.log("b != a:", "b" != "a");

console.log("=================================");

//ESTRITAMENTE IGUAL ===
console.log("ESTRITAMENTE IGUAL");

console.log("10 === 10:", "10" === 10);
console.log("10 === 10:", "10" === "10");

console.log("=================================");

//ESTRITAMENTE DIFERENTE !===
console.log("ESRITAMENTE DIFERENTE");

console.log("10 !== 10:", "10" !== 10);
console.log("10 !== 10:", "10" !== "10");

console.log("=================================");

//OPERADOR MAIOR >
console.log("OPERADOR MAIOR >");

console.log("10 > 11", 10 > 11);
console.log("20 > 11", 20 > 11);

console.log("=================================");

//OPERADOR MENOR <

console.log("OPERADOR MENOR < ");

console.log("10 < 11", 10 < 11);
console.log("20 < 11", 20 < 11);

console.log("=================================");

//OPERADOR MAIOR >=
console.log("OPERADOR MAIOR >= ");

console.log("10 >= 11", 10 >= 11);
console.log("11 >= 11", 11 >= 11);

console.log("=================================");

//OPERADOR MENOR <=
console.log("OPERADOR MENOR <= ");

console.log("10 <= 11", 10 <= 11);
console.log("11 <= 11", 11 <= 11);